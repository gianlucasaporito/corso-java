package srl.neotech.autosalone.model;

public class CercaAutoRequest {

private String cercaModello;
private String cercaCostruttore;
private Integer cercaCosto;
private String cercaAlimentazione;
private String cercaTarga;

public String getCercaModello() {
    return cercaModello;
}
public void setCercaModello(String cercaModello) {
    this.cercaModello = cercaModello;
}
public String getCercaCostruttore() {
    return cercaCostruttore;
}
public void setCercaCostruttore(String cercaCostruttore) {
    this.cercaCostruttore = cercaCostruttore;
}
public Integer getCercaCosto() {
    return cercaCosto;
}
public void setCercaCosto(Integer cercaCosto) {
    this.cercaCosto = cercaCosto;
}
public String getCercaAlimentazione() {
    return cercaAlimentazione;
}
public void setCercaAlimentazione(String cercaAlimentazione) {
    this.cercaAlimentazione = cercaAlimentazione;
}
public String getCercaTarga() {
    return cercaTarga;
}
public void setCercaTarga(String cercaTarga) {
    this.cercaTarga = cercaTarga;
}

}
