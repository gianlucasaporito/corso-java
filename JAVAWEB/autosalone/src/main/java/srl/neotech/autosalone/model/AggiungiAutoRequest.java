package srl.neotech.autosalone.model;

public class AggiungiAutoRequest {
private String inputModello;
private String inputCostruttore;
private Integer inputCosto;
private String inputAlimentazione;
private String inputTarga;

public String getInputModello() {
    return inputModello;
}
public void setInputModello(String inputModello) {
    this.inputModello = inputModello;
}
public String getInputCostruttore() {
    return inputCostruttore;
}
public void setInputCostruttore(String inputCostruttore) {
    this.inputCostruttore = inputCostruttore;
}

public Integer getInputCosto() {
    return inputCosto;
}
public void setInputCosto(Integer inputCosto) {
    this.inputCosto = inputCosto;
}
public String getInputAlimentazione() {
    return inputAlimentazione;
}
public void setInputAlimentazione(String inputAlimentazione) {
    this.inputAlimentazione = inputAlimentazione;
}
public String getInputTarga() {
    return inputTarga;
}
public void setInputTarga(String inputTarga) {
    this.inputTarga = inputTarga;
}


}
