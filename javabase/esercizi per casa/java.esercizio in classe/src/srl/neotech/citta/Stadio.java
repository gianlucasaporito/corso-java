package srl.neotech.citta;

public class Stadio extends Squadra {
    private Integer numerPosti;
    private String  nomeStadio;
    private String  indirizzo;
    public Integer getNumerPosti() {
        return numerPosti;
    }
    public void setNumerPosti(Integer numerPosti) {
        this.numerPosti = numerPosti;
    }
    public String getNomeStadio() {
        return nomeStadio;
    }
    public void setNomeStadio(String nomeStadio) {
        this.nomeStadio = nomeStadio;
    }
    public String getIndirizzo() {
        return indirizzo;
    }
    public void setIndirizzo(String indirizzo) {
        this.indirizzo = indirizzo;
    }
    
}
